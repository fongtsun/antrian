export class QueueRoom {
  constructor(state, env) {
    this.state = state;
    this.env = env;
    this.sessions = new Set();
    this.state.blockConcurrencyWhile(async () => {
      this.data = (await this.state.storage.get("state")) || {
        darah: 0, dokter: 0, lastDarah: null, lastDokter: null
      };
    });
  }

  async fetch(req) {
    const url = new URL(req.url);

    if (req.headers.get("Upgrade") === "websocket") {
      const pair = new WebSocketPair();
      const client = pair[0];
      const server = pair[1];

      server.accept();
      this.sessions.add(server);

      server.addEventListener("message", (evt) => {
        try {
          const msg = JSON.parse(evt.data);
          if (msg.type === "ping") return;
          if (msg.type === "action") {
            this.handleAction(msg.action, msg.payload || {});
          }
        } catch (e) { console.error("WS msg error", e); }
      });

      const cleanup = () => this.sessions.delete(server);
      server.addEventListener("close", cleanup);
      server.addEventListener("error", cleanup);

      // Send initial state
      setTimeout(() => {
        try {
          server.send(JSON.stringify({ type: "state", state: this.data }));
        } catch (e) { console.error("send initial failed", e); }
      }, 0);

      return new Response(null, { status: 101, webSocket: client });
    }

    if (url.pathname === "/action" && req.method === "POST") {
      try {
        const { action, payload } = await req.json();
        this.handleAction(action, payload || {});
        return new Response("OK");
      } catch (_) {
        return new Response("Bad Request", { status: 400 });
      }
    }

    return new Response("Not found", { status: 404 });
  }

  handleAction(action, payload) {
    let s = this.data;
    let speakType = null;

    if (action === "callDarah") {
      s.darah++; s.lastDarah = s.darah; speakType = "callDarah";
    } else if (action === "recallDarah" && s.lastDarah) {
      s.darah = s.lastDarah; speakType = "recallDarah";
    } else if (action === "resetDarah") {
      s.darah = 0; s.lastDarah = null; speakType = "resetDarah";
    } else if (action === "callDokter") {
      s.dokter++; s.lastDokter = s.dokter; speakType = "callDokter";
    } else if (action === "recallDokter" && s.lastDokter) {
      s.dokter = s.lastDokter; speakType = "recallDokter";
    } else if (action === "resetDokter") {
      s.dokter = 0; s.lastDokter = null; speakType = "resetDokter";
    } else if (action === "setStart" && payload) {
      if (payload.startDarah !== undefined) {
        const v = parseInt(payload.startDarah, 10);
        if (!Number.isNaN(v)) { s.darah = v; s.lastDarah = v; }
      }
      if (payload.startDokter !== undefined) {
        const v2 = parseInt(payload.startDokter, 10);
        if (!Number.isNaN(v2)) { s.dokter = v2; s.lastDokter = v2; }
      }
      speakType = "setStart";
    }

    this.state.storage.put("state", s);
    this.broadcast({ type: "state", state: s, action, speak: speakType });
  }

  broadcast(msg) {
    const data = JSON.stringify(msg);
    for (const ws of Array.from(this.sessions)) {
      try { ws.send(data); } catch { this.sessions.delete(ws); }
    }
  }
}

export default {
  async fetch(req, env) {
    const url = new URL(req.url);

    if (req.headers.get("Upgrade") === "websocket") {
      const id = env.QUEUE_ROOM.idFromName("default");
      const stub = env.QUEUE_ROOM.get(id);
      return stub.fetch(req);
    }

    if (url.pathname === "/action") {
      const id = env.QUEUE_ROOM.idFromName("default");
      const stub = env.QUEUE_ROOM.get(id);
      return stub.fetch(req);
    }

    const assetName = PAGES[url.pathname];
    if (assetName) {
      const ext = assetName.split('.').pop();
      const headers = { "content-type": contentTypeFromExt(ext), "cache-control": "no-store" };

      if (assetName in TEXT_ASSETS) {
        return new Response(TEXT_ASSETS[assetName], { headers });
      }
      if (assetName in BINARY_ASSETS) {
        return new Response(BINARY_ASSETS[assetName], { headers });
      }
      return new Response("Not found", { status: 404 });
    }

    if (url.pathname === "/") {
      const headers = { "content-type": "text/html; charset=utf-8", "cache-control": "no-store" };
      return new Response(TEXT_ASSETS["index.html"], { headers });
    }

    return new Response("Not found", { status: 404 });
  }
};
